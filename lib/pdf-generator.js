import jsPDF from 'jspdf'
import autoTable from 'jspdf-autotable'

// Generate a professional engineering report PDF.
// type: one of 'beam' | 'column' | 'slab' | 'concrete-volume' | 'steel-weight'
export function generateCalculationPDF({ type, inputs, result, user, calculationId }) {
  const doc = new jsPDF({ unit: 'mm', format: 'a4' })
  const W = doc.internal.pageSize.getWidth()

  // ----- Header band -----
  doc.setFillColor(15, 23, 42) // slate-900
  doc.rect(0, 0, W, 28, 'F')
  doc.setFillColor(249, 115, 22) // orange-500
  doc.rect(0, 28, W, 1, 'F')

  doc.setTextColor(255, 255, 255)
  doc.setFont('helvetica', 'bold')
  doc.setFontSize(18)
  doc.text('CivilCalc Pro', 14, 14)
  doc.setFont('helvetica', 'normal')
  doc.setFontSize(9)
  doc.setTextColor(203, 213, 225)
  doc.text('AI-Powered Civil Engineering Platform', 14, 20)

  const titleMap = {
    'beam': 'RCC Beam Design Report',
    'column': 'RCC Column Design Report',
    'one-way-slab': 'One-Way Slab Design Report',
    'slab': 'One-Way Slab Design Report',
    'concrete-volume': 'Concrete Volume & Materials Report',
    'steel-weight': 'Steel Weight Calculation Report',
  }
  const title = titleMap[type] || 'Calculation Report'

  doc.setTextColor(15, 23, 42)
  doc.setFont('helvetica', 'bold')
  doc.setFontSize(15)
  doc.text(title, 14, 40)
  doc.setFont('helvetica', 'normal')
  doc.setFontSize(9)
  doc.setTextColor(100, 116, 139)
  doc.text(`Design Code: IS 456:2000 · Generated: ${new Date().toLocaleString('en-IN')}`, 14, 46)
  if (user) doc.text(`Engineer: ${user.name || user.email}`, 14, 51)
  if (calculationId) doc.text(`Calc ID: ${calculationId}`, 14, 56)

  let y = 64

  // ----- Inputs table -----
  const inputRows = Object.entries(inputs || {}).map(([k, v]) => [
    humanize(k),
    String(v),
  ])
  autoTable(doc, {
    startY: y,
    head: [['Input Parameter', 'Value']],
    body: inputRows,
    theme: 'grid',
    headStyles: { fillColor: [15, 23, 42], textColor: 255, fontStyle: 'bold' },
    styles: { fontSize: 9, cellPadding: 2.5 },
    margin: { left: 14, right: 14 },
  })
  y = doc.lastAutoTable.finalY + 8

  // ----- Results: walk nested object and create one table per section -----
  const sections = flattenResult(result)
  for (const { title, rows } of sections) {
    if (y > 250) {
      doc.addPage()
      y = 20
    }
    doc.setFont('helvetica', 'bold')
    doc.setFontSize(11)
    doc.setTextColor(249, 115, 22)
    doc.text(title, 14, y)
    y += 3
    autoTable(doc, {
      startY: y,
      body: rows,
      theme: 'striped',
      styles: { fontSize: 9, cellPadding: 2.5 },
      columnStyles: { 0: { textColor: [71, 85, 105] }, 1: { fontStyle: 'bold', textColor: [15, 23, 42] } },
      margin: { left: 14, right: 14 },
    })
    y = doc.lastAutoTable.finalY + 6
  }

  // ----- Safety verdict (if any) -----
  if (typeof result?.isDesignSafe === 'boolean') {
    if (y > 250) {
      doc.addPage()
      y = 20
    }
    const safe = result.isDesignSafe
    const bg = safe ? [220, 252, 231] : [254, 243, 199]
    const fg = safe ? [22, 101, 52] : [146, 64, 14]
    doc.setFillColor(bg[0], bg[1], bg[2])
    doc.roundedRect(14, y, W - 28, 16, 2, 2, 'F')
    doc.setFont('helvetica', 'bold')
    doc.setFontSize(11)
    doc.setTextColor(fg[0], fg[1], fg[2])
    doc.text(safe ? 'DESIGN IS SAFE — OK per IS 456:2000' : 'DESIGN REQUIRES REVIEW', 18, y + 10)
    y += 22
  }

  // ----- Footer disclaimer on every page -----
  const pageCount = doc.getNumberOfPages()
  for (let i = 1; i <= pageCount; i++) {
    doc.setPage(i)
    doc.setFont('helvetica', 'normal')
    doc.setFontSize(7)
    doc.setTextColor(148, 163, 184)
    doc.text(
      'Generated by CivilCalc Pro — Calculations are guidance only. Always verify designs per IS codes and project conditions.',
      14,
      290
    )
    doc.text(`Page ${i} of ${pageCount}`, W - 14, 290, { align: 'right' })
  }

  const filename = `civilcalc-${type}-${(calculationId || Date.now()).toString().slice(-8)}.pdf`
  doc.save(filename)
}

function humanize(key) {
  return key
    .replace(/([A-Z])/g, ' $1')
    .replace(/^./, (s) => s.toUpperCase())
    .replace(/_/g, ' ')
}

function flattenResult(result) {
  if (!result || typeof result !== 'object') return []
  const sections = []
  for (const [groupKey, groupValue] of Object.entries(result)) {
    if (groupKey === 'isDesignSafe') continue
    if (groupValue && typeof groupValue === 'object' && !Array.isArray(groupValue)) {
      const rows = Object.entries(groupValue).map(([k, v]) => [
        humanize(k),
        formatValue(v),
      ])
      sections.push({ title: humanize(groupKey), rows })
    }
  }
  // Top-level scalar values
  const topRows = Object.entries(result)
    .filter(([k, v]) => k !== 'isDesignSafe' && (typeof v !== 'object' || v === null))
    .map(([k, v]) => [humanize(k), formatValue(v)])
  if (topRows.length) sections.unshift({ title: 'Summary', rows: topRows })
  return sections
}

function formatValue(v) {
  if (v === true) return 'Yes'
  if (v === false) return 'No'
  if (v === null || v === undefined) return '—'
  return String(v)
}
